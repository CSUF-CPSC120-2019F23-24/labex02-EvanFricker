// Name: Diamond
// This program displays a diamond pattern to the screen.

#include <iostream>

int main()
{
  std::cout << "    *    " << '\n';   // Lines 8-14 will display a diamond when function is activated
  std::cout << "   ***    " << '\n';
  std::cout << "  *****    " << '\n';
  std::cout << " *******   " << '\n';
  std::cout << "  *****    " << '\n';
  std::cout << "   ***    " << '\n';
  std::cout << "    *    " << '\n';
  return 0;
}
